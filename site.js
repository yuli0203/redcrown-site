const RTL = new Set(['he']);
const rm = !!(window.matchMedia && matchMedia('(prefers-reduced-motion: reduce)').matches);
const PAGE_LANG = document.documentElement.getAttribute('data-page-lang') || 'en';

function applyLang(lang, persist){
  const dict = I18N[lang] || I18N.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = RTL.has(lang) ? 'rtl' : 'ltr';
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const v = dict[el.getAttribute('data-i18n')];
    if (v != null) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el=>{
    const v = dict[el.getAttribute('data-i18n-aria')];
    if (v != null) el.setAttribute('aria-label', v);
  });
  if (dict['doc.title']) document.title = dict['doc.title'];
  // Show the active language on the toggle; keep only the others in the menu.
  const active = document.querySelector('.lang-menu button[data-lang="'+lang+'"]');
  const slot = document.querySelector('.lang-cur');
  if (active && slot) slot.innerHTML = active.innerHTML;
  document.querySelectorAll('.lang-menu button').forEach(b=>{ b.hidden = (b.dataset.lang === lang); });
  if (persist) { try { localStorage.setItem('rc_lang', lang); } catch(e){} }
}

// Match the visitor's browser/device language to a supported one, else English.
function detectLang(){
  const list = (navigator.languages && navigator.languages.length)
    ? navigator.languages : [navigator.language || 'en'];
  for (const l of list){
    const code = (l || '').slice(0,2).toLowerCase();
    if (I18N[code]) return code;
  }
  return 'en';
}

